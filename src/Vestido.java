public class Vestido {
    
    public int id;
    public int tamanho;
    public String cor;
    public String marca;
    public String finalidade;
    public double valor;
    
    public Vestido(int id, int tamanho, String cor, String marca, String finalidade, double valor) {
        this.id = id;
        this.tamanho = tamanho;
        this.cor = cor;
        this.marca = marca;
        this.finalidade = finalidade;
        this.valor = valor;
    }

}
